# Q. Computing and printing out (x + y) * (x + y)

# Asking the user to enter integer inputs
int1 = int(input("Enter the first number: "))
int2 = int(input("Enter the second number: "))

# computing and printing out the square of sum
square_of_sum = (int1 + int2) ** 2

print(f"The square of the sum of the 2 integers is: {square_of_sum}" )